const mongoose = require('mongoose');

const registerSchema = new mongoose.Schema({

    name: {
        type: String,
        required: true
    },
    email: {
        type: String,
        required: true
    },
    password: {
        type: String,
        required: true
    },
    confirmPassword: {
        type: String
    }
});

module.exports = mongoose.model('registerUser', registerSchema);